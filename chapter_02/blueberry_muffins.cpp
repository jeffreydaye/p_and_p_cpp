#include "../std_lib_facilities.h"

int main()
{
    cout << "********** BLUEBERRY MUFFIN RECIPE **********\n\n";

    cout << "***** INFORMATION *****\n"
    << "Servings: 12\n"
    << "Preparation Time: 15 minutes\n"
    << "Cooking Time: 30 minutes\n\n";

    cout << "***** INGREDIENTS *****\n" 
    << "- 2cups all purpose flour\n"
    << "- 2tsp baking powder\n"
    << "- 3/4tsp salt\n"
    << "- 1 stick (or 1/2 cup) unsalted butter, softened\n"
    << "- 2 large eggs\n"
    << "- 1-1/2tsp vanilla extract\n"
    << "- 1cup granulated sugar\n"
    << "- 1/4tsp almond extract\n"
    << "- 1/2cup milk\n"
    << "- 2-1/4cup fresh buleberries\n"
    << "- 2tbsp turbinado sugar (raw sugar)\n\n";

    cout << "***** COOKWARE/UTENSILS *****\n"
    << "12 cup Muffin Pan\n"
    << "Medium Mixing Bowl\n"
    << "Electric Mixer\n"
    << "Ice Cream Scoop (optional)\n"
    << "Cooling Rack\n"
    << "Non-stick cooking spray\n"
    << "12 Paper muffin liners\n\n";

    cout << "***** INSTRUCTIONS *****\n"
    << "1. Preheat the oven to 375 degrees Fahrenheit.\n"
    << "2. Line a 12 cup muffin pan with paper muffin liners.\n"
    << "3. Spray the muffin pan and muffin liners with non-stick cooking spray.\n"
    << "4. In a medium mixing bowl, whisk together the flour, baking soda and salt.\n"
    << "5. In the bowl of the electric mixer, beat the butter and granualted sugar for about 2 minutes.\n"
    << "6. Add eggs one at a time to electric mixer bowl, scraping down the sides of the bowl and beating well after each addition.\n"
    << "7. Beat in vanilla extract and almond extract.\n"
    << "8. Gradually add the flour mixture to the electric mixer bowl, alternating with milk. Mixing on a low speed to combine.\n"
    << "9. Add blueberries to the final batter.\n"
    << "10. Fold in blueberries gently with a spatula until evenly distributed. Do not overmix.\n"
    << "11. Scoop the  prepared batter into the prepared muffin pan using an ice cream scoop if one is available.\n"
    << "12. Sprinkle the turbinado sugar eveny over the top of each muffin.\n"
    << "13. Put muffins in oven and bake for 30 minutes, until lightly golden.\n"
    << "14. Allow muffins to cool in pan for 10 minutes AFTER baking.\n"
    << "15. Using a knife, gently remove each muffin and place it on a cooling rack for final cooling.\n\n";

    cout << "***** NUTRITION *****\n"
    << "Serving:        1 muffin\n"
    << "Calories:       252 cal\n"
    << "Fat:            9g\n"
    << "Saturated Fat:  5g\n"
    << "Carbohydrates:  39g\n"
    << "Sugar:          22g\n"
    << "Fiber:          1g\n"
    << "Protein:        4g\n"
    << "Sodium:         224mg\n"
    << "Cholesterol:    52mg\n\n";

    return 0;
}